jQuery.fn.rollover = function(suffix) {
    suffix = suffix || '_on';
    var check = new RegExp(suffix + '\\.\\w+$');
    return this.each(function() {
        var img = jQuery(this);
        var src = img.attr('src');
        if (check.test(src)) return;
        var _on = src.replace(/\.\w+$/, suffix + '$&');
        jQuery('<img>').attr('src', _on);
        img.hover(
            function() {
                img.attr('src', _on);
            },
            function() {
                img.attr('src', src);
            }
        );
    });
};
 





